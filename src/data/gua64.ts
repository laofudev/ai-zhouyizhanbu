// 根据卦象编码获取卦信息
export function getGuaByCode(code: number): GuaInfo | null {
  return LIU_SHI_SI_GUA[code] || null;
}

// 根据爻数组获取卦象编码
// yaos: 从下到上，阳爻=true，阴爻=false
export function getGuaCode(yaos: boolean[]): number {
  let code = 0;
  for (let i = 0; i < 6; i++) {
    if (yaos[i]) {
      code |= (1 << i);
    }
  }
  return code;
}

// 获取变卦编码（变爻取反）
export function getChangedGuaCode(yaos: boolean[], changedIndices: number[]): number {
  let code = 0;
  for (let i = 0; i < 6; i++) {
    let isYang = yaos[i];
    // 如果是变爻，取反
    if (changedIndices.includes(i)) {
      isYang = !isYang;
    }
    if (isYang) {
      code |= (1 << i);
    }
  }
  return code;
}
